	LDA BULLET_Y \ SEC \ SBC #4 \ STA BULLET_Y \ CMP #24 \ BCC B_OFF 
	TAY \ LDA $4180,Y \ ORA #$03 \ STA $4180,Y \ JMP JUMP_LOGIC 
