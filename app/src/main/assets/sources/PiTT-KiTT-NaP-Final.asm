	LDA JUMP_DIR \ BNE J_ACT \ LDA PORTA \ AND #$02 \ BNE DRAW_PHASE \ LDA #1 \ STA JUMP_DIR \ JMP DRAW_PHASE
J_ACT
	CMP #1 \ BEQ J_UP \ CMP #3 \ BEQ J_HANG
J_DWN
	DEC JUMP_VAL \ LDA JUMP_VAL \ BNE J_S_U \ STA JUMP_DIR \ LDA #0 \ STA AUDC2 \ JMP DRAW_PHASE
J_UP
	INC JUMP_VAL \ LDA JUMP_VAL \ CMP #12 \ BCC J_S_U \ LDA #3 \ STA JUMP_DIR \ LDA #12 \ STA JUMP_TIMER \ JMP J_S_U
J_HANG
	DEC JUMP_TIMER \ BNE J_S_U \ LDA #2 \ STA JUMP_DIR
J_S_U
	LDA #$A2 \ STA AUDC2 \ LDA JUMP_VAL \ EOR #$FF \ STA AUDF2

